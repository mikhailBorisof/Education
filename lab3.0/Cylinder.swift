    }
    // –≤—ã—á–∏—Å–ª—è–µ–º–æ–µ —Å–≤–æ–π—Å—Ç–≤–æ –±–æ'–ª—å—à–∏–π –¥–∏–∞–º–µ—Ç—Ä —Å–µ—á–µ–Ω–∏—è —Ü–∏–ª–∏–Ω–¥—Ä–∞
