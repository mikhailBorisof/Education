        if radius == nil || height1 == nil || height2 == nil { return nil }
        return Double.pi * radius! * radius! * (height2! + height1! ) / 2
    }
    // –≤—ã—á–∏—Å–ª—è–µ–º–æ–µ —Å–≤–æ–π—Å—Ç–≤–æ –ø–ª–æ—â–∞–¥—å —Å–µ—á–µ–Ω–∏—è —Ü–∏–ª–∏–Ω–¥—Ä–∞
    var sectionSquare: Double? {
        if radius == nil || height1 == nil || height2 == nil { return nil }
